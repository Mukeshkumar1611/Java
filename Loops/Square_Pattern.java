import java.util.Scanner;
public class Square_pattern {


	public static void main(String[] args) {
		
		Scanner mk = new Scanner(System.in);
		System.out.print("Enter Number: ");
        int n = mk.nextInt();
        for(int i=0;i<n;i++){
            for(int j=0;j<n;j++){
                System.out.print(n);
            }
            System.out.println();
        }
		
	}

}
